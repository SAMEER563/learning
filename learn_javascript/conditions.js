// switch statement

let number = 2;

switch(number) {
    case 1 : console.log("One");
    break;
    case 2 : console.log("Two");
    break;
    case 3 : console.log("Three");
    break;
    case 4 : console.log("Four");
    break;
    default : console.log("Invalid number");
}

