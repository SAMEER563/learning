// Declare array 

let arr = [12, 50, "Ram", true]
console.log(arr)

// Push operation - Add new element at the end of array
arr.push("Shalu");
console.log(arr);

// Pop operation - Remove last element from the array
arr.pop();
console.log(arr)

// Unshift method - Use to add new element at starting 
arr.unshift("sam")
console.log(arr)